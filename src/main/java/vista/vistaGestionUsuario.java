    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        try {

            if (txtContrasena.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Digita la contraseña para continuar");
                return;
            }

            int contrasena = Integer.parseInt(txtContrasena.getText());
            if (cl.eliminar(contrasena)) {
                JOptionPane.showMessageDialog(null, "Se borró el usuario de la base de datos");
                this.setVisible(false);
                inicio = new vistaUsuario();
                inicio.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Error al eliminar el usuario");
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Formato de contraseña incorrecto, asegurate de ingresar datos numéricos");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

