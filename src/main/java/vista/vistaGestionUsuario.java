    private void btnCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarActionPerformed
        try {

            if (txtContrasena.getText().isEmpty() || txtNombre.getText().isEmpty() || txtApellido.getText().isEmpty() || txtCorreo.getText().isEmpty() || txtCedula.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Llena todo el formulario para continuar");
                return;
            }

            int contrasena = Integer.parseInt(txtContrasena.getText());
            String nombre = txtNombre.getText();
            String apellido = txtApellido.getText();
            String correo = txtCorreo.getText();
            int cedula = Integer.parseInt(txtCedula.getText());

            if (cl.modificar(contrasena, nombre, apellido, correo, cedula)) {
                JOptionPane.showMessageDialog(null, "Se modificó el usuario en la base de datos");
            } else {
                JOptionPane.showMessageDialog(null, "Error al modificar el usuario");
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Formato de contarena o cedula incorrectos, asegurate de ingresar datos numéricos");
        }
    }//GEN-LAST:event_btnCargarActionPerformed

